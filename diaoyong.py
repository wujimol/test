class diaowin:
    def senshow(self, senwin, win):
        senwin.show()
        win.hide()

    def show(self, win, senwin):
        win.show()
        senwin.hide()

    def itshow(self, win, itwin):
        itwin.show()
        win.hide()

    def show2(self, win, itwin):
        itwin.hide()
        win.show()
